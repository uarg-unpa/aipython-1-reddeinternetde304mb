texto = "sometimes it is the people no one imagines anything of who do the things that no one can imagine."
texto_capitalize = texto.capitalize()
texto_title = texto.title()
texto_swapcase = texto.swapcase()
texto_formateado = texto.capitalize().title().swapcase()

