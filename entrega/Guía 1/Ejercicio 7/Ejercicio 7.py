num1 = int(input("Porfavor ingrese un numero entero: "))
num2 = float(input("Porfavor ingrese un numero racional (ejemplo: 2.5): "))

print(f"{num1} + {num2} = {num1 + num2}")
print(f"{num1} - {num2} = {num1 - num2}")
print(f"{num1} * {num2} = {num1 * num2}")
print(f"{num1} ** {num2} = {num1 ** num2}")
print(f"{num1} // {num2} = {num1 // num2}")