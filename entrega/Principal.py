print("El", "Taller", "Es", sep="\n")
#Newline: los distintos argumentos se imprimen en una nueva linea
print("Esto debería ser una nueva línea", end=", ")
print("Pero no lo es")
#end="" hace que al final del argumento, lo siguiente no se imprima en una nueva línea,
#sino que ese \n se imprima seguidamente

