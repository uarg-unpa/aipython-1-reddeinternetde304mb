num_a = float(input("Porfavor ingrese dos números.\nNúmero 1: "))
num_b = float(input("Número 2: "))

if num_a > num_b:
    print(f"El número {num_a} es mayor que {num_b}")
elif num_a < num_b:
    print(f"El número {num_a} es menor que {num_b}")
elif num_a == num_b:
    print(f"El número {num_a} es igual a {num_b}")